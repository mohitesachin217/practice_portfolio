                    <h3>The idea behind the design</h3>
                    <p>
                        The andreas03 template has a small file size, a simple layout for easy editing, good accessibility features and semantic markup. Five small images have been used for the design, adding only 3 kilobytes to the template load size (which is approximately 12kb).
                    </p>

                    <p>
                        The images are linked from the stylesheet, 
                        which means that they will not show up in browsers that does not support CSS. 
                        They can also be safely removed, since the images do not affect the layout in any way. 
                        In CSS-enabled browsers the removed images will be replaced by a background color. 
                        In browsers with no CSS support, there will be no difference. See examples of this by looking at the alternate versions listed under the menu.
                    </p>
