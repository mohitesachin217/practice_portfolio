                    <h3>The idea behind the design:</h3>
                    <p>
                        When it was time to redesign one of my sites, I got an idea about making one single design that all of my sites could use, and make some kind of official link network between the sites. My way of doing this turned out to be the use of toptabs (for selecting a website) and navitabs (for selecting subsections within the different sites). With a basic PHP template, all sites could now have the same design and share the same toptabs - but they can have their own separate content. If needed, each navitab can have a submenu in the sidebar, which allows this design to handle hundreds of pages while still keeping a quick and easy navigation. To make navigation more logical, each tab can have its own colorscheme. The "personal" site has blue colors, the "company" site is green, the "artist" site is red and so on...
                    </p>

                    <p class="content-notice">
                        <strong>Please note:</strong> I have not aimed at making any kind of trendy or shiny look for this design. I prefer to keep it clean and simple, and let others add the details that will make the design special and unique. However, the design has some useful built-in features, such as a print CSS and full functionality when degrading to no-CSS mode. You can use the design as it is, but I recommend that you play around with it. Add things, remove things, let your creativity flow. Andreas02 gives you a solid structure to build from.
                    </p>
